import vars from "./configs/vars.config";
import mongooseConfig from "./configs/mongoose.config";
import { globalLogger } from "./configs/logger.config";
import { app } from "./app";
import { createChannel, createConsumer, createProducer, getUserRMQ } from "./services/userMq.service";
import { ConsumeMessage } from "amqplib";
import { Channel } from "amqp-connection-manager";

const discoverChannel =  createChannel(vars.rabbitMq.disCoverRMQKey!);
const userChannel = createChannel(vars.rabbitMq.userRMQKey!);
setInterval(async () => {
  await createConsumer(discoverChannel,"1291oiewiolkd9283.discover", async (msg: ConsumeMessage, channelWrapper: Channel) => {
    if (msg.content.toString()) {
      const data = await getUserRMQ(msg.content.toString());
      channelWrapper.ack(msg);
      await createProducer(userChannel,"209i13q0iewodjwqaikmd.user", data!);
    }
  });
}, 1000);

const PORT = vars.server.Port || 3000;

mongooseConfig(vars.mongoUrl)
  .then(() => {
    app.listen(PORT, () => console.log(`Listening on port ${PORT}`));
  })
  .catch((err) => {
    globalLogger.error(err);
    console.log({ err });
  });
